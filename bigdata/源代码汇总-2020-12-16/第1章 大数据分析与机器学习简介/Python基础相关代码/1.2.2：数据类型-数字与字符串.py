a = 1
print(type(a))
a = '1'
print(type(a))

# 将数字转换成字符串
a = 1
b = str(a)  # 将数字转换成字符串，并赋值给变量b
c = b + '1'
print(c)

# 将字符串转换成数字
a = '1'
b = int(a)  # 将字符串转换成数字，并赋值给变量b
c = b + 1
print(c)

